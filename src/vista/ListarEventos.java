    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //boton buscar
        
        int ID;
        String nombre, comentario;
        String fecha;
        
        DefaultTableModel modelo = (DefaultTableModel) this.jtb_table.getModel();
        
        modelo.setRowCount(0); //Filas en 0
        
        List<Evento> eventos = repositorioEvento.buscarTodos();
        
        for (Evento evento : eventos) {
            ID = evento.getIDevento();
            nombre = evento.getNombre();
            fecha = evento.getFecha().toString(); //Transformamos de dato a String
            comentario = evento.getNota();
            
            modelo.addRow(new Object []{ID,nombre,fecha,comentario});
            
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

