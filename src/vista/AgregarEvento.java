    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton Agregar evento => obtener datos de botones y pedir que sean ingresados los datos
        
      
        try {
            
            Date fecha =  this.jDateChooser1.getDate();
           
            String nombre = this.jtx_nombreEvento.getText();
            if(nombre.isEmpty()){
                JOptionPane.showMessageDialog(this, "Debes ingresar un nombre", "Validacion", 1);
                this.jtx_nombreEvento.requestFocus();
                return;
            }
            String nota = this.jtx_nota.getText();
            int idUsuario = SesionUsuario.Usuario.getIDusuario();
            Evento e = new Evento(nombre, fecha, nota, idUsuario); 
            
            // guardar datos en repositorio
            repositorioEvento.agregarEvento(e);
            // enviar notificacion de evento guardado correctamente
            JOptionPane.showMessageDialog(this, "Se agrego Evento de forma exitosa", "Aviso", 1);
        
        } catch (HeadlessException e) {
            
            JOptionPane.showMessageDialog(this, "Â¡Debes ingresar los datos!", "Aviso", 1);
           
        }
        // limpiar forma
        this.jtx_nombreEvento.setText("");
        this.jtx_nota.setText("");      

    }//GEN-LAST:event_jButton1ActionPerformed

